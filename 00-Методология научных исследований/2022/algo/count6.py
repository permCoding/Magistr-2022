# Кубик бросили 10 раз. Какое наиболее вероятное кол-во раз выпадет 6-ка?

from random import randint as r

# n = 10
n = 4
arr = [0] * (n+1)
for _ in range(10_000):
    lst = [r(1,6) for _ in range(n)]
    arr[lst.count(6)] += 1
print(arr)

